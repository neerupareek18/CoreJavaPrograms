package LiveClassPrograms_15072024;

public class Lab017 {
    public static void main(String[] args) {
        // Compound Assignment Operators
        int a = 10; //Assignment, === is not available in JAVA
        // += , -=, /=, *= . %=
        // += ->
        a+=10; // a = a+10
        a-=10; // a = a-10
        a/=10; // a = a/10
        a*=10; // a = a*10
        a%=8;
        System.out.println(a);
    }
}
