package PracticeQuestions_21072024;

public class Lab093 {
    public static void main(String[] args) {
        int a =10;
        System.out.println(a+=10); //a=20
        System.out.println(a*=10); //a=200
        System.out.println(a/=10); //a=20
        System.out.println(a%=10); //a=0
        System.out.println(a-=10); //a=-10
    }
}
